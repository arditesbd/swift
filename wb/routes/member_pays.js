var express = require('express');
var router = express.Router();
var member_pay=require('../models/member_pay');

router.get('/:no?',function(req,res,next){

if(req.params.no){

    member_pay.getmember_payByNo(req.params.no,function(err,rows){

        if(err)
        {
            res.json(err);
        }
        else{
            res.json(rows);
        }
    });
}
else{

 member_pay.getAllmember_pays(function(err,rows){

        if(err)
        {
            res.json(err);
        }
        else
        {
            res.json(rows);
        }
 
    });
}
});
